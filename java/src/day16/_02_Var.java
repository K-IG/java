package day16;

public class _02_Var {

	public static void main(String[] args) {
		// Variable(변수) : 자주 사용되는 혹은 자주 변경될 수 있는 값을 저장하는 공간
		// Type(자료형) : qustn(저장공간)의 형태 : 공간을 생성할때만 써준다.
		
		
		//숫자형 변수
		int var = 0;
		
		//상수형ㅇ 변수
		double var2 = 0.0;
		
		//문자열형 변수
		String var3 = null;
		
		//qnfflsgud qustn
		boolean var4 = false;
		
		
		
		System.out.println(var);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
		
	
	}
	
	
	

}
